let bgBlue = document.querySelector('.blue');
let bgRed = document.querySelector('.red');
let bgYellow = document.querySelector('.yellow');

bgBlue.addEventListener('click', function bgBlue() {
    document.body.style.backgroundColor = 'blue';
});
bgRed.addEventListener('click', function bgRed() {
    document.body.style.backgroundColor = 'red';
});
bgYellow.addEventListener('click', function bgYellow() {
    document.body.style.backgroundColor = 'yellow';
});





